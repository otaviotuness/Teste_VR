    public void readTable() throws SQLException{
    
        DefaultTableModel modelo = (DefaultTableModel) TabAluno.getModel();
        AlunoDAO cdao = new AlunoDAO();
        modelo.setRowCount(0);
        
        for (ClassAluno c : cdao.read()) {
            
             modelo.addRow(new Object[]{
                c.getCodigo(),
                c.getMatricula(),
                c.getNome(),
                c.getRg(),
                c.getCpf()
             }
             );   
        }
    }
    
    public void refreshCombo() throws SQLException{
        
        DefaultComboBoxModel comboBoxModel = (DefaultComboBoxModel) cb_Curso.getModel();
        CursoDAO cursodao = new CursoDAO();
        
        for (model.ClassCurso clscurso : cursodao.read()){ 
            comboBoxModel.addElement(clscurso);    
        }
    }
    
    private void clean(){
        edtMatricula.setText(null);
        edt_CPF.setText(null);
        edt_Nome.setText(null);
        edt_CPF.setText(null);
        edt_RG.setText(null);
    }
    
    private void charge(){
        
        if (TabAluno.getSelectedRow() != -1){
            
            edtMatricula.setText(TabAluno.getValueAt(TabAluno.getSelectedRow(), 1).toString());
            edt_Nome.setText(TabAluno.getValueAt(TabAluno.getSelectedRow(), 2).toString());
            edt_RG.setText(TabAluno.getValueAt(TabAluno.getSelectedRow(), 3).toString());
            edt_CPF.setText(TabAluno.getValueAt(TabAluno.getSelectedRow(), 4).toString());
        }
    }
    
