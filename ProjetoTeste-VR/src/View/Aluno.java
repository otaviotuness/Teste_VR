    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        ClassAluno clsaluno = new ClassAluno();
        AlunoDAO dao = new AlunoDAO();
        clsaluno.setMatricula(edtMatricula.getText());
        clsaluno.setNome(edt_Nome.getText());
        clsaluno.setCpf(edt_CPF.getText());
        clsaluno.setRg(edt_RG.getText());
        
        ClassCurso curso = (ClassCurso) cb_Curso.getSelectedItem();
        
        int idCurso = curso.getCodigo();
        
        clsaluno.setCurso(idCurso);

        try {
            dao.create(clsaluno);
        } catch (SQLException ex) {
            Logger.getLogger(Curso.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            readTable();
        } catch (SQLException ex) {
            Logger.getLogger(Curso.class.getName()).log(Level.SEVERE, null, ex);
        }

        clean();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (TabAluno.getSelectedRow() != -1){

            ClassAluno clsaluno = new ClassAluno();
            AlunoDAO dao = new AlunoDAO();
            clsaluno.setMatricula(edtMatricula.getText());
            clsaluno.setNome(edt_Nome.getText());
            clsaluno.setRg(edt_RG.getText());
            clsaluno.setCpf(edt_CPF.getText());
            
            ClassCurso curso = (ClassCurso) cb_Curso.getSelectedItem();
        
            int idCurso = curso.getCodigo();

            clsaluno.setCurso(idCurso);

            clsaluno.setCodigo((int) TabAluno.getValueAt(TabAluno.getSelectedRow(), 0));
            try {
                dao.update(clsaluno);
            } catch (SQLException ex) {
                Logger.getLogger(Curso.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                readTable();
            } catch (SQLException ex) {
                Logger.getLogger(Curso.class.getName()).log(Level.SEVERE, null, ex);
            }
            clean();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (TabAluno.getSelectedRow() != -1){

            ClassAluno c = new ClassAluno();
            AlunoDAO dao = new AlunoDAO();
            c.setCodigo((int) TabAluno.getValueAt(TabAluno.getSelectedRow(), 0));
            try {
                dao.delete(c);
            } catch (SQLException ex) {
                Logger.getLogger(Curso.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                readTable();
            } catch (SQLException ex) {
                Logger.getLogger(Curso.class.getName()).log(Level.SEVERE, null, ex);
            }
            clean();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

